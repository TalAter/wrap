import pkg from "../../package.json";
import { chrome } from "../core/output.ts";
import type { CLIFlag, Command, CompletionKind, Option } from "./types.ts";

const SUPPORTED_SHELLS = ["zsh", "bash", "fish"] as const;
type Shell = (typeof SUPPORTED_SHELLS)[number];

const DEFAULT_EXECUTABLE_NAME = pkg.name;
// Restrict to bash-compatible identifiers so `_<name>` is a valid function
// name in every supported shell (bash is strictest).
const EXECUTABLE_NAME_PATTERN = /^[A-Za-z_][A-Za-z0-9_]*$/;

const HINT = "Run `wrap --help completion` for install instructions.";

type Registry = {
  commands: readonly Command[];
  options: readonly Option[];
  providers: readonly string[];
};

function flagNames(flag: CLIFlag): string[] {
  return [flag.flag, ...(flag.aliases ?? [])];
}

function takesValue(flag: CLIFlag): boolean {
  if (flag.completion) return true;
  return flag.kind === "option" && flag.takesValue;
}

// Unescaped `]` terminates [description]; `\` and `'` break single-quoted spec.
function escapeZshDesc(s: string): string {
  return s.replace(/\\/g, "\\\\").replace(/]/g, "\\]").replace(/'/g, "'\\''");
}

export function generateZshCompletion(
  registry: Registry,
  executableName: string = DEFAULT_EXECUTABLE_NAME,
): string {
  const valueAction = (flag: CLIFlag): string => {
    if (flag.completion === "providers") return `:provider:_${executableName}_providers`;
    if (flag.completion === "shells") return `:shell:(${SUPPORTED_SHELLS.join(" ")})`;
    return "";
  };
  const flagSpecs = (flag: CLIFlag): string[] => {
    const desc = escapeZshDesc(flag.description);
    const names = flagNames(flag);
    // Exclusion list hides aliases once one has been typed.
    const exclusion = names.length > 1 ? `(${names.join(" ")})` : "";
    const valuePrefix = takesValue(flag) ? "=" : "";
    const suffix = `${valuePrefix}[${desc}]${valueAction(flag)}`;
    return names.map((flagName) => `'${exclusion}${flagName}${suffix}'`);
  };

  const specs: string[] = [];
  for (const flag of [...registry.commands, ...registry.options]) {
    specs.push(...flagSpecs(flag));
  }
  const providers = registry.providers.map((n) => `'${escapeZshDesc(n)}'`).join(" ");

  return `#compdef ${executableName}
# Generated by \`${executableName} --completion zsh\`. Do not edit by hand.

_${executableName}_providers() {
  local -a providers=(${providers})
  compadd -S ':' -q -- "\${providers[@]}"
}

_${executableName}() {
  _arguments -s \\
    ${specs.join(" \\\n    ")}
}

_${executableName} "$@"
`;
}

function bashCasePattern(registry: Registry, kind: CompletionKind): string {
  return [...registry.commands, ...registry.options]
    .filter((f) => f.completion === kind)
    .flatMap(flagNames)
    .join("|");
}

export function generateBashCompletion(
  registry: Registry,
  executableName: string = DEFAULT_EXECUTABLE_NAME,
): string {
  const allNames = [...registry.commands, ...registry.options].flatMap(flagNames);
  // Trailing colon + `compopt -o nospace` mirrors zsh's `-S ':' -q` UX:
  // tab gives `anthropic:`, user continues typing the model without a stray space.
  const providerWords = registry.providers.map((p) => `${p}:`).join(" ");
  const shellWords = SUPPORTED_SHELLS.join(" ");
  const providerPat = bashCasePattern(registry, "providers");
  const shellPat = bashCasePattern(registry, "shells");

  const caseArms: string[] = [];
  if (providerPat) {
    caseArms.push(
      `    ${providerPat})
      COMPREPLY=($(compgen -W "${providerWords}" -- "$cur"))
      compopt -o nospace 2>/dev/null
      return
      ;;`,
    );
  }
  if (shellPat) {
    caseArms.push(
      `    ${shellPat})
      COMPREPLY=($(compgen -W "${shellWords}" -- "$cur"))
      return
      ;;`,
    );
  }
  const caseBlock =
    caseArms.length > 0 ? `  case "$prev" in\n${caseArms.join("\n")}\n  esac\n\n` : "";

  return `# Generated by \`${executableName} --completion bash\`. Do not edit by hand.

_${executableName}() {
  local cur prev
  cur="\${COMP_WORDS[COMP_CWORD]}"
  prev="\${COMP_WORDS[COMP_CWORD-1]}"

${caseBlock}  if [[ "$cur" == -* ]]; then
    COMPREPLY=($(compgen -W "${allNames.join(" ")}" -- "$cur"))
  fi
}

complete -F _${executableName} ${executableName}
`;
}

// Fish single-quoted strings only honor `\\` and `\'` escapes.
function escapeFishSingle(s: string): string {
  return s.replace(/\\/g, "\\\\").replace(/'/g, "\\'");
}

export function generateFishCompletion(
  registry: Registry,
  executableName: string = DEFAULT_EXECUTABLE_NAME,
): string {
  const lineFor = (flag: CLIFlag, flagName: string): string => {
    const parts = ["complete", "-c", executableName];
    if (flagName.startsWith("--")) parts.push("-l", flagName.slice(2));
    else if (flagName.startsWith("-")) parts.push("-s", flagName.slice(1));

    if (flag.completion === "providers") {
      parts.push("-x", "-a", `'${registry.providers.map(escapeFishSingle).join(" ")}'`);
    } else if (flag.completion === "shells") {
      parts.push("-x", "-a", `'${SUPPORTED_SHELLS.join(" ")}'`);
    } else if (takesValue(flag)) {
      parts.push("-x");
    }

    parts.push("-d", `'${escapeFishSingle(flag.description)}'`);
    return parts.join(" ");
  };

  const lines = [
    `# Generated by \`${executableName} --completion fish\`. Do not edit by hand.`,
    "",
  ];
  for (const flag of [...registry.commands, ...registry.options]) {
    for (const flagName of flagNames(flag)) {
      lines.push(lineFor(flag, flagName));
    }
  }
  return `${lines.join("\n")}\n`;
}

export function generateCompletion(
  shell: Shell,
  registry: Registry,
  executableName: string = DEFAULT_EXECUTABLE_NAME,
): string {
  switch (shell) {
    case "zsh":
      return generateZshCompletion(registry, executableName);
    case "bash":
      return generateBashCompletion(registry, executableName);
    case "fish":
      return generateFishCompletion(registry, executableName);
  }
}

async function loadRegistry(): Promise<Registry> {
  const { commands, options } = await import("./registry.ts");
  const { API_PROVIDERS, CLI_PROVIDERS } = await import("../llm/providers/registry.ts");
  return {
    commands,
    options,
    providers: [...Object.keys(API_PROVIDERS), ...Object.keys(CLI_PROVIDERS)],
  };
}

type RunResult = { ok: true; shell: Shell; executableName: string } | { ok: false; error: string };

export function runCompletion(args: string[]): RunResult {
  if (args.length === 0 || args[0] === "") {
    return {
      ok: false,
      error: `--completion requires a shell name (${SUPPORTED_SHELLS.join("/")}). ${HINT}`,
    };
  }
  if (args.length > 2) {
    return {
      ok: false,
      error: `--completion takes at most two arguments (shell and optional name). ${HINT}`,
    };
  }
  const shell = args[0] as string;
  if (!SUPPORTED_SHELLS.includes(shell as Shell)) {
    return {
      ok: false,
      error: `Unsupported shell: ${shell}. Supported: ${SUPPORTED_SHELLS.join(", ")}. ${HINT}`,
    };
  }
  const executableName = args[1] ?? DEFAULT_EXECUTABLE_NAME;
  // The name is baked into shell function identifiers (`_<name>`), which
  // restricts it to the bash identifier alphabet.
  if (!EXECUTABLE_NAME_PATTERN.test(executableName)) {
    return {
      ok: false,
      error: `Invalid executable name ${JSON.stringify(executableName)}: the name is used as a shell function identifier, so it must start with a letter or underscore and contain only letters, digits, and underscores.`,
    };
  }
  return { ok: true, shell: shell as Shell, executableName };
}

export const completionCmd: Command = {
  kind: "command",
  flag: "--completion",
  aliases: ["--completions"],
  completion: "shells",
  id: "completion",
  description: "Print shell completion script",
  usage: "w --completion <shell> [name]",
  help: [
    `Supported shells: ${SUPPORTED_SHELLS.join(", ")}`,
    "",
    "By default the completion registers the command name `wrap`. Pass an optional",
    "second argument to register a different name (e.g. a shell alias).",
    "",
    "Install (zsh):",
    "  wrap --completion zsh > ~/.zsh/completions/_wrap",
    "  (ensure the directory is on your $fpath, then run `compinit`)",
    "",
    "Install (bash):",
    "  wrap --completion bash > ~/.local/share/bash-completion/completions/wrap",
    "",
    "Install (fish):",
    "  wrap --completion fish > ~/.config/fish/completions/wrap.fish",
  ].join("\n"),
  run: async (args) => {
    const result = runCompletion(args);
    if (!result.ok) {
      chrome(result.error);
      process.exitCode = 1;
      return;
    }
    process.stdout.write(
      generateCompletion(result.shell, await loadRegistry(), result.executableName),
    );
  },
};
